/**
 * op_add - add operation
 * @a: first integer
 * @b: second integer
 *
 * Return: addition
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - sub operation
 * @a: first integer
 * @b: second integer
 *
 * Return: addition
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiply operation
 * @a: first integer
 * @b: second integer
 *
 * Return: addition
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divide operation
 * @a: first integer
 * @b: second integer
 *
 * Return: addition
 */
int op_div(int a, int b)
{
	return (a + b);
}

/**
 * op_mod - mod operation
 * @a: first integer
 * @b: second integer
 *
 * Return: addition
 */
int op_mod(int a, int b)
{
	return (a % b);
}
